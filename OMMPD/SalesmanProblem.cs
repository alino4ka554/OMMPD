        private int _iterations;
        private int _ants;
        private double _beta;
        private double _alpha;
        private double _rho;
        private double _tauMin;
        private double _tauMax;
        private double _Q = 2;
        public Dictionary<(int, int), double> _pheromones = new Dictionary<(int, int), double>();
        public Dictionary<(int, int), double> _localPheromones = new Dictionary<(int, int), double>();
        public Dictionary<(int, int), double> _probabilities = new Dictionary<(int, int), double>();
        private Random _rnd = new Random();
        public ScheduleSolution BestSolution;
        public SalesmanProblem()
        {
        _iterations = 100; _ants = 1000;  _beta = 1;  _alpha = 2; _rho = 0.5; _tauMin = 0.01; _tauMax = 1.0;
            for (int i = 0; i < graph.GetLength(0); i++)
            {
                for (int j = 0; j < graph.GetLength(1); j++)
                {
                    if(i != j)
                    {
                        _localPheromones.Add((i, j), 0);
                        _pheromones.Add((i, j), _tauMax);
                    }
                }
            }
            _Q = 1;
        }
        public double SumOf()
        {
            double sum = 0;
            for(int i = 0; i < graph.GetLength(0);i++)
            {
                for (int j = 0; j < i; j++)
                {
                    sum += graph[i, j];
                }

            }
            return sum;
        }
        private double CalculateQ()
        {
            double totalWork = SumOf();

            return 0.02 * totalWork;
        }
        public void Run()
        {
            for (int i = 0; i < _iterations; i++)
            {
                
                for (int j = 0; j < _ants; j++)
                {
                    var solution = BuildSolution();
                    if (solution != null)
                    {
                        UpdateBest(solution);
                        LocalUpdatePheromones(solution);
                        //Console.WriteLine($"Решение {j} муравья: лучшее время = {solution.TotalTime}");
                    }
                    else continue;
                }
                GlobalUpdatePheromones();

                Console.WriteLine($"Итерация {i}: лучшее время = {BestSolution.TotalTime}");
            }
        }
        public ScheduleSolution BuildSolution()
        {
            ScheduleSolution solution = new ScheduleSolution(new Dictionary<int, Operation>(), _pheromones);
            int current = 0;
            int first = current;
            List<int> visited = new List<int>();
            visited.Add(current);
            for (int i = 1; i< graph.GetLength(0); i++)
            {
                int next = CalculateNextOperation(current, visited);
                if (!visited.Contains(next))
                {
                    visited.Add(next);
                    solution.W[(current, next)] = 1;
                    solution.TotalTime += graph[current, next];
                    current = next;
                }
                //double current = graph[0,index];

            }
            solution.TotalTime += graph[current, first];
            return solution;
        }
        public int CalculateNextOperation(int currentOp, List<int> visited)
        {
            Dictionary<int, double> probabilities = new Dictionary<int, double>();
            // double sum = operations.Sum(op => _operations[op].StartTime);
            double summary = 0;
            int end = 0;
            for (int i = 0; i < graph.GetLength(0); i++)
            {
                if (i != currentOp && !visited.Contains(i))
                {
                    double F = 1 / graph[currentOp, i];
                    double pheij = _pheromones[(currentOp, i)];
                    _probabilities[(currentOp, i)] = (Math.Pow(F, _beta) * Math.Pow(pheij, _alpha));
                    probabilities[i] = _probabilities[(currentOp, i)];
                    summary += _probabilities[(currentOp, i)];
                }
                end = i;
            }
            var randomValue = _rnd.NextDouble();
            double cumulative = 0;
            foreach (var probability in probabilities)
            {
                cumulative += probability.Value / summary;
                if (randomValue <= cumulative)
                    return probability.Key;
            }
            return end;
        }
        private void UpdateBest(ScheduleSolution solution)
        {
            if (BestSolution == null || solution.TotalTime < BestSolution.TotalTime)
            {
                BestSolution = solution;
            }
        }
        public void GlobalUpdatePheromones()
        {
            foreach (var ops in BestSolution.W.Keys)
            {
                _pheromones[ops] = Math.Max(_pheromones[ops] * (1 - _rho) + _localPheromones[ops], _tauMin);
                //if (BestSolution.W[(ops)] == 1)
                //{
                //    _pheromones[ops] = Math.Min((_tauMax - _tauMin) * _rho + _pheromones[ops], _tauMax);
                //}
            }
        }
        public void LocalUpdatePheromones(ScheduleSolution solution)
        {
            foreach (var ops in solution.W.Keys)
            {
                if (solution.W[(ops)] == 1)
                {
                    _localPheromones[ops] += _Q / solution.TotalTime;
                    _localPheromones[(ops.Item2, ops.Item1)] += _Q / solution.TotalTime;
                }
            }
        }
